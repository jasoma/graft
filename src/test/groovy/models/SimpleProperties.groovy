package models

/**
 * The simplest possible node model, only contains basic types directly supported by neo4j.
 */
class SimplePropertiesNode {

    boolean aBoolean
    String aString
    long aLong
    double aDouble

}

/**
 * The simplest possible relation model, only contains basic types directly supported by neo4j.
 */
class SimplePropertiesRelation {

    boolean aBoolean
    String aString
    long aLong
    double aDouble

}
